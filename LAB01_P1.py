# -- Crie um programa que lê 6 valores inteiros, armazene-os em uma lista e, ---------------
# -- em seguida, percorra essa listra mostrando na tela cada um dos valores salvos. --------
# ------------------------------------------------------------------------------------------
# -- Resolução -----------------------------------------------------------------------------
listNum = []                           # -- Cria a lista vazia -----------------------------
qtdNum = 6                             # -- Quantidade de numeros --------------------------
# ------------------------------------------------------------------------------------------
# -- Preenche a lista ----------------------------------------------------------------------
for c in range(qtdNum):                # -- Para c em qtdNum -------------------------------
    listNum.append(int(input('Digite o %dº número: ' %(c+1)))) # ---------------------------
# ------------------------------------------------------------------------------------------
# -- Mostrar os resultados obtidos ---------------------------------------------------------
print(listNum) # ---------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------
# -- Fim  ----------------------------------------------------------------------------------